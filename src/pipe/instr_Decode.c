comb_logic_t
extract_regs(uint32_t insnbits, opcode_t op, 
             uint8_t *src1, uint8_t *src2, uint8_t *dst) {
    switch(op) {
        //M 
        case OP_LDUR:
            *src1 = bitfield_u32(insnbits, 5, 5);
            *dst = bitfield_u32(insnbits, 0, 5);
        case OP_STUR:
            *src1 = bitfield_u32(insnbits, 5, 5);
            *src2 = bitfield_u32(insnbits, 0, 5);
            break;
        //I1
        case OP_MOVK:
        case OP_MOVZ:
            *dst = bitfield_u32(insnbits, 0, 5);
            break;
        //RI
        case OP_ADD_RI:
        case OP_SUB_RI:
        case OP_LSL:
        case OP_LSR:
        case OP_UBFM:
        case OP_ASR:
            *src1 = bitfield_u32(insnbits, 5, 5);
            *dst = bitfield_u32(insnbits, 0, 5);
            break;
        //RR
        case OP_ADDS_RR:
        case OP_SUBS_RR:
        case OP_CMP_RR: 
        case OP_MVN:
        case OP_ORR_RR:
        case OP_EOR_RR:
        case OP_ANDS_RR:
        case OP_TST_RR:
            *src1 = bitfield_u32(insnbits, 5, 5);
            *src2 = bitfield_u32(insnbits, 16, 5);
            *dst = bitfield_u32(insnbits, 0, 5);
            break;
        //B3
        case OP_RET:
            *src1 = bitfield_u32(insnbits, 5, 5);
            break;
        default:
            break;
    }
    return;
}

